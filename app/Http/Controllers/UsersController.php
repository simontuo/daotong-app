     */
    public function update(Request $request, $id)
    {
        $user = $this->user->byId($id);

        if ($user->id == user()->id) {
            $user->update($user->merge($request->all()));

            alert()->success('编辑用户信息成功')->autoclose(2000);

            return back();
        }

        abort(404);
    }

